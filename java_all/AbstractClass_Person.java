public abstract class AbstractClass_Person
{
    private String pname,pgen;
    private int age;
    public void setPname(String s)
    {
        pname=s;
    }
    public String getPname()
    {
        return pname;
    }
    public void setPgen(String g)
    {
        pgen=g;
    }
    public String getGen()
    {
        return pgen;
    }
    public void setAge(int a)
    {
        age=a;
    }
    public int getAge()
    {
        return age;
    }
    
} 